function copyCode() {
    const copyText = document.getElementById('output-bbcode');
    const textToCopy = copyText.value;

    // Função interna para mostrar feedback visual
    const showFeedback = () => {
        const msg = document.getElementById('copy-msg');

        msg.classList.remove('hidden', 'toast-animate-out');
        msg.classList.add('toast-animate-in');

        setTimeout(() => {
            msg.classList.remove('toast-animate-in');
            msg.classList.add('toast-animate-out');

            setTimeout(() => {
                msg.classList.add('hidden');
                msg.classList.remove('toast-animate-out');
            }, 300);
        }, 2000);
    };

    // Tentar API moderna
    if (navigator.clipboard && navigator.clipboard.writeText) {
        navigator.clipboard.writeText(textToCopy).then(() => {
            showFeedback();
        }).catch(err => {
            console.error('Erro ao usar clipboard API: ', err);
            fallbackCopy(textToCopy);
        });
    } else {
        fallbackCopy(textToCopy);
    }

    function fallbackCopy(text) {
        try {
            // Criar elemento temporário visível
            const textArea = document.createElement("textarea");
            textArea.value = text;

            // Garantir que o elemento seja parte do DOM mas não atrapalhe o layout
            textArea.style.position = "fixed";
            textArea.style.left = "-9999px";
            textArea.style.top = "0";
            document.body.appendChild(textArea);

            textArea.focus();
            textArea.select();

            const successful = document.execCommand('copy');
            document.body.removeChild(textArea);

            if (successful) {
                showFeedback();
            } else {
                console.error('Falha ao copiar com execCommand');
            }
        } catch (err) {
            console.error('Erro no fallback de cópia: ', err);
        }
    }
}

let errorTimeout;
function showError(message) {
    const errorMsg = document.getElementById('error-msg');
    const errorText = document.getElementById('error-text');

    if (errorText) errorText.textContent = message;

    if (errorMsg) {
        // Clear previous timeout to prevent race conditions
        if (errorTimeout) clearTimeout(errorTimeout);

        errorMsg.classList.remove('hidden', 'toast-animate-out');
        errorMsg.classList.add('toast-animate-in');

        // Adicionar animação de shake para chamar atenção
        errorMsg.classList.add('shake');
        setTimeout(() => errorMsg.classList.remove('shake'), 500);

        errorTimeout = setTimeout(() => {
            errorMsg.classList.remove('toast-animate-in');
            errorMsg.classList.add('toast-animate-out');

            setTimeout(() => {
                errorMsg.classList.add('hidden');
                errorMsg.classList.remove('toast-animate-out');
                errorTimeout = null;
            }, 300);
        }, 3000);
    }
}

function validateForm(tipo) {
    let isValid = true;
    let fieldsToCheck = [];

    // Limpar estilos de erro anteriores
    document.querySelectorAll('.invalid-field').forEach(el => el.classList.remove('invalid-field'));

    if (tipo === 'destaques_ranking') {
        fieldsToCheck = [
            'input-dr-inicio', 'input-dr-fim', 'input-dr-banner',
            'input-dr-nick1', 'input-dr-texto1',
            'input-dr-nick2', 'input-dr-texto2',
            'input-dr-nick3', 'input-dr-texto3'
        ];
    } else if (tipo === 'destaques_melhores') {
        fieldsToCheck = [
            'input-dm-inicio', 'input-dm-fim', 'input-dm-banner',
            'input-dm-nick1', 'input-dm-texto1',
            'input-dm-nick2', 'input-dm-texto2'
        ];
    }

    fieldsToCheck.forEach(id => {
        const field = document.getElementById(id);
        if (field && !field.value.trim()) {
            isValid = false;
            field.classList.add('invalid-field');
        }
    });

    if (!isValid) {
        showError('Por favor, preencha todos os campos obrigatórios.');
    }

    return isValid;
}

// Função de envio para grupos
async function enviarMensagemGrupo(tipo) {
    if (isSending) return;

    // Validar antes de enviar
    if (!validateForm(tipo)) return;

    const template = templates[tipo];
    const groups = template.groups;
    const btn = document.getElementById(`btn-send-${tipo}`);
    const btnText = document.getElementById(`btn-${tipo}-text`);
    const originalText = btnText.innerText;
    const progress = document.getElementById(`${tipo}-progress`);
    const progressBar = document.getElementById(`${tipo}-progress-bar`);
    const progressText = document.getElementById(`${tipo}-progress-text`);
    const statusContainer = document.getElementById(`${tipo}-status`);

    // Desabilitar sidebar durante o envio
    disableSidebar();

    const bbcode = generateBBCode(tipo);
    // Também preencher o textarea de output para o caso de erro (para copiar)
    document.getElementById('output-bbcode').value = bbcode;

    const subject = template.subject;

    btn.disabled = true;
    isSending = true;
    btn.classList.add('opacity-50', 'cursor-not-allowed');
    btnText.innerText = 'Enviando...';
    progress.classList.remove('hidden');
    statusContainer.innerHTML = '';

    let enviados = 0, erros = 0;
    let failedGroups = [];

    for (let i = 0; i < groups.length; i++) {
        const group = groups[i];
        addStatusRow(`${tipo}-status`, group.id, group.name, '', group.logo);

        try {
            const formData = new FormData();
            formData.append('usergroup', group.id);
            formData.append('subject', subject);
            formData.append('message', bbcode);
            formData.append('mode', 'post');
            formData.append('post', '1');

            const response = await fetch('/privmsg', { method: 'POST', body: formData });
            if (response.ok) {
                enviados++;
                updateStatusRow(`${tipo}-status`, group.id, 'success');
            } else {
                throw new Error();
            }
        } catch (e) {
            erros++;
            failedGroups.push(group.name);
            updateStatusRow(`${tipo}-status`, group.id, 'error');
        }

        progressBar.style.width = `${((i + 1) / groups.length) * 100}%`;
        progressText.innerText = `${i + 1}/${groups.length}`;

        if (i < groups.length - 1) await new Promise(r => setTimeout(r, 5000));
    }

    // Safe guard to ensure sidebar unlock if something catastrophic happens
    try {
        if (erros === 0) {
            btnText.innerText = 'Todas enviadas!';
            btn.classList.add('bg-green-600');

            // Celebrar!
            if (typeof confetti === 'function') {
                confetti({
                    particleCount: 150,
                    spread: 100,
                    origin: { y: 0.6 },
                    colors: ['#8B0000', '#DC2626', '#ffffff']
                });
            }

            // Mostrar modal de sucesso
            setTimeout(() => {
                const successModal = document.getElementById('modal-success');
                successModal.classList.remove('hidden');
                successModal.querySelector('.modal-animate').classList.remove('modal-animate-out');
                successModal.querySelector('.modal-animate').classList.add('modal-animate');
            }, 500);

        } else {
            btnText.innerText = `${enviados}/${groups.length} enviadas`;
            btn.classList.add('bg-yellow-600');

            // Mostrar modal de erro com lista
            setTimeout(() => {
                const errorModal = document.getElementById('modal-error-send');
                const listContainer = document.getElementById('failed-groups-list');
                listContainer.innerHTML = failedGroups.map(name => `<div class="mb-1">• ${name}</div>`).join('');

                errorModal.classList.remove('hidden');
                errorModal.querySelector('.modal-animate').classList.remove('modal-animate-out');
                errorModal.querySelector('.modal-animate').classList.add('modal-animate');
            }, 500);
        }
    } catch (err) {
        console.error('Erro ao finalizar envio:', err);
        // Ensure UI unlock on error
        enableSidebar();
    } finally {
        setTimeout(() => {
            btn.disabled = false;
            isSending = false;
            btn.classList.remove('opacity-50', 'cursor-not-allowed', 'bg-green-600', 'bg-yellow-600');
            btnText.innerText = originalText;
            progress.classList.add('hidden');
        }, 5000);
    }
}

function closeResultModal(modalId) {
    const modal = document.getElementById(modalId);
    const content = modal.querySelector('div'); // First child div

    content.classList.remove('modal-animate');
    content.classList.add('modal-animate-out');

    // Desbloquear sidebar ao fechar o modal
    enableSidebar();

    setTimeout(() => {
        modal.classList.add('hidden');
        content.classList.remove('modal-animate-out');
    }, 300);
}

// Helpers de Status
function addStatusRow(containerId, id, title, subtitle = '', logo = '') {
    const container = document.getElementById(containerId);
    const rowId = `status-row-${String(id).replace(/\s+/g, '-')}`;
    const html = `
        <div id="${rowId}" class="flex items-center gap-3 bg-black/30 p-3 rounded-xl border border-white/5 transition-all">
            ${logo ? `<img src="${logo}" alt="${title}" class="h-8 w-8 object-contain rounded">` : `
            <div class="status-icon w-8 h-8 rounded-full bg-yellow-500/20 flex items-center justify-center">
                <i class="fa-solid fa-spinner fa-spin text-yellow-400"></i>
            </div>`}
            <div class="flex-1">
                <p class="text-sm font-bold text-white">${title}</p>
                ${subtitle ? `<p class="text-[10px] text-gray-500 uppercase font-bold">${subtitle}</p>` : ''}
            </div>
            ${logo ? `<span class="status-icon"><i class="fa-solid fa-spinner fa-spin text-yellow-400"></i></span>` : `<span class="status-text text-[10px] font-bold text-yellow-500 uppercase tracking-widest">Enviando</span>`}
        </div>
    `;
    container.insertAdjacentHTML('beforeend', html);
}

function updateStatusRow(containerId, id, status) {
    const rowId = `status-row-${String(id).replace(/\s+/g, '-')}`;
    const element = document.getElementById(rowId);
    if (!element) return;

    const iconDiv = element.querySelector('.status-icon');
    const statusText = element.querySelector('.status-text');

    if (status === 'success') {
        if (element.querySelector('img')) {
            element.className = 'flex items-center gap-3 p-3 rounded-xl border bg-[#8B0000]/20 border-[#8B0000]/40 text-[#EF4444] transition-all';
            iconDiv.innerHTML = '<i class="fa-solid fa-check h-5 w-5"></i>';
        } else {
            iconDiv.className = 'status-icon w-8 h-8 rounded-full bg-green-500/20 flex items-center justify-center';
            iconDiv.innerHTML = '<i class="fa-solid fa-check text-green-400"></i>';
            statusText.className = 'status-text text-[10px] font-bold text-green-400 uppercase tracking-widest';
            statusText.innerText = 'Sucesso';
            element.classList.add('border-green-500/20');
        }
    } else {
        if (element.querySelector('img')) {
            element.className = 'flex items-center gap-3 p-3 rounded-xl border bg-red-500/20 border-red-500/40 text-red-400 transition-all';
            iconDiv.innerHTML = '<i class="fa-solid fa-xmark h-5 w-5"></i>';
        } else {
            iconDiv.className = 'status-icon w-8 h-8 rounded-full bg-red-500/20 flex items-center justify-center';
            iconDiv.innerHTML = '<i class="fa-solid fa-xmark text-red-400"></i>';
            statusText.className = 'status-text text-[10px] font-bold text-red-400 uppercase tracking-widest';
            statusText.innerText = 'Erro';
            element.classList.add('border-red-500/20');
        }
    }
}

// Modal close on click outside
window.onclick = function (event) {
    const modal = document.getElementById('modal');
    if (event.target == modal) {
        closeModal();
    }
}

// Lógica de Menu Mobile
const mobileMenuBtn = document.getElementById('mobile-menu-btn');
const closeSidebarBtn = document.getElementById('close-sidebar-btn');
const sidebar = document.getElementById('sidebar');
const sidebarOverlay = document.getElementById('sidebar-overlay');

function toggleMobileMenu(show) {
    if (show) {
        sidebar.classList.remove('-translate-x-full');
        sidebarOverlay.classList.remove('hidden');
        document.body.style.overflow = 'hidden';
    } else {
        sidebar.classList.add('-translate-x-full');
        sidebarOverlay.classList.add('hidden');
        document.body.style.overflow = '';
    }
}

mobileMenuBtn.addEventListener('click', () => toggleMobileMenu(true));
closeSidebarBtn.addEventListener('click', () => toggleMobileMenu(false));
sidebarOverlay.addEventListener('click', () => toggleMobileMenu(false));

